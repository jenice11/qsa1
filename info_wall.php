									<h3>Kids, Teenagers Smoking</h3>
									<p>The overwhelming majority of today's daily smokers began smoking before they were 18 years old. Tobacco use during childhood and adolescence not only can cause major health problems, but people who begin smoking at an early age are more likely to develop a severe addiction than those who start at a later age.</p>
									<h3>How to Keep Kids from Smoking</h3>
<p>Keeping children and teenagers from starting smoking is critical and they need your help. Youth smoking can be prevented by the combined efforts of families, schools, communities and policymakers. For example:

<ul><li>Parents can set a positive example for their children by not smoking themselves, and keeping their homes smokefree.</li>
<li>Schools can provide tobacco prevention programs to educate students about the dangers of smoking and tobacco cessation programs to help young people who are already addicted quit smoking for good.</li>
<li>States can pass legislation to increase taxes on tobacco products, pass and implement comprehensive smokefree indoor air laws, and limit minors' access to tobacco products.</li></ul></p>
