								<div class="container">
									<h3>Effects of Tabacco</h3>
									<p>On your body</p>
									<ul><li>Hair
										<p>Just as nicotine has negative effects on lungs and circulation, it also has negative effects on hair. Smoking is linked to premature graying and loss of hair.</p></li>
										<li>Brain
										<p>Nicotine changes your brain. The brain develops extra nicotine receptors in response to the nicotine in tobacco.
											When your brain stops getting the nicotine it’s used to, you experience nicotine withdrawal. Smoking also increases
											the amount of build-up in your arteries, which may block the flow of blood to your brain, causing a stroke.</p></li>
										<li>Mouth &amp; Teeth
											<p>Mouth: Bad breath and losing your sense of taste are well known consequences of tobacco use for your mouth, but
				there are even more serious ones. Using tobacco is the single greatest risk factor for oral cancer.&nbsp;</p>
			<p>Teeth: Yellow discoloration is only the first issue to occur in teeth. If you smoke a pack a day, you can lose two or more teeth every ten years, according to the Academy of General Dentists. </p></li>
										<li>Throat
										<p>Cigarettes contain formaldehyde, ammonia and hydrogen sulfide, which can cause serious irritation to the nose and
				throat. This results in a runny nose and causes ‘smoker’s cough.’ Continued exposure can produce unusual thickening
				in the throat lining, a condition that has been linked to throat cancer.</p></li>

										<li>Lungs
										<p>Cigarette smoke attacks the lung’s natural defenses and can completely paralyze the natural cleansing process.
				Excess mucus in the lungs will make you more likely to get colds, flu, bronchitis and other respiratory infections.
				Continued exposure can lead to lung cancer and lung diseases – including pneumonia and emphysema. Research indicates
				smoking causes 90 percent of lung cancer in men and 80 percent in women. More women die from lung cancer than breast
				cancer.</p></li>
										<li>Heart
										<p>Cigarette smokers have a higher risk of developing cardiovascular disease. Smoking causes you to lose some of your
				good cholesterol and raises your blood pressure. It also increases the risk of blood clotting. All of these can lead
				to coronary heart disease.</p></li>
										<li>Digestive System
										<p>Smokers are at greater risk of developing peptic ulcers, Crohn’s disease and gallstones. They are also more likely
				to experience chronic heartburn. In addition, smoking changes the way the liver operates, including how it processes
				alcohol.</p></li>

										<li>Skin
										<p>Smoking deprives the skin of oxygen and nutrients. This is why some smokers appear pale, while others develop uneven coloring. These changes can begin at a young age.</p></li>
										<li>Hands
										<p>The loss of blood circulation in your hands from smoking causes numbness, and makes your hands cold to the touch.
				The nicotine in cigarettes can also leave you with permanently yellow-stained fingers and fingernails.</p></li>
				<li>Reproductive System
					<p>Male: The more you smoke the higher your risk of impotence. One study showed that men who smoked more than a pack
				of day had a 60 percent higher risk of erectile dysfunction, compared to men who never smoked.</p>
			<p>Female: Women who smoke may also have a harder time getting pregnant. They also have a higher chance of losing
				their baby before it is born. Studies show there is an increased risk of SIDS (sudden infant death syndrome, also
				called ‘crib death’) in babies born to female smokers.</p></li>
				<li>Bones
					<p>Ingredients in cigarette smoke disrupt the natural cycle of bone health. Your body is less able to form healthy
				new bone tissue, and it breaks down existing bone tissue more rapidly. Over time, smoking leads to a thinning of
				bone tissue and loss of bone density. This causes bones to become weak and brittle. Compared to nonsmokers, smokers
				have a higher risk of bone fractures, and their broken bones take longer to heal.</p></li>
				<li>Feet
					<p>People who smoke one and a half packs a day or more are most likely to develop Buerger's disease. Buerger’s
				disease affects blood vessels in the arms and legs. Blood vessels swell, which can prevent blood flow and cause
				clots to form. This can lead to pain, tissue damage and even gangrene (the death or decay of body tissues). In some
				cases, amputation may be required.</p></li>

